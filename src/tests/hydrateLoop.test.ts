/**
 * Tests for the reunified playlist hydrate loop.
 *
 * The hydrate loop delegates per-video work to fetchAndStoreVideo using
 * preFetchedDetails so no extra videos.list calls are issued.
 *
 * Acceptance criteria:
 *   1. fetchAndStoreVideo is invoked once per video (delegation works).
 *   2. No videos.list (batchFetchVideoDetails) calls happen during the
 *      per-video transcript pass — details are reused via preFetchedDetails.
 *      (Implicit: if the metadata path was hit, unknown test IDs would
 *      trigger a real network request and fail loudly.)
 *   3. Classification errors are routed through classifyTranscriptError
 *      (indirectly: a "No captions" error produces transcript_status='missing').
 *   4. API quota used by the hydrate pass = ceil(N/50) videos.list calls
 *      (0 per-video when preFetchedDetails is supplied).
 *   5. DB rows written via the delegated fetchAndStoreVideo carry channel_id.
 *
 * Uses the `transcriptFetcher` test seam on fetchAndStoreVideo to inject a
 * mock transcript function without needing ESM module mocking.
 */
import { describe, it, before } from "node:test";
import assert from "node:assert/strict";
import path from "node:path";
import fs from "node:fs";
import os from "node:os";

// Route the singleton DB at require-time to a clean tmp file so getDb() works.
const tmpDbPath = path.join(
  os.tmpdir(),
  `yt-hydrateLoop-${process.pid}-${Date.now()}.db`
);
fs.writeFileSync(tmpDbPath, "");
process.env.YOUTUBE_MCP_DB_PATH = tmpDbPath;

const { initSchema } = await import("../db/schema.js");
const { getDb } = await import("../db/connection.js");
const { fetchAndStoreVideo } = await import("../tools/get-video-details.js");
import type { VideoDetails, Transcript } from "../types.js";

function makeDetails(id: string): VideoDetails {
  return {
    videoId: id,
    title: `Title ${id}`,
    channelId: `UC_${id}`,
    channelTitle: "Test Channel",
    description: "desc",
    publishedAt: "2024-01-01T00:00:00Z",
    thumbnailUrl: "",
    duration: "5m",
    tags: [],
    categoryId: "22",
    statistics: { viewCount: "0", likeCount: "0", commentCount: "0" },
  };
}

function makeTranscript(id: string): Transcript {
  return {
    videoId: id,
    language: "en",
    isAutoGenerated: false,
    segments: [{ text: "hello", start: 0, duration: 1 }],
    fullText: "hello",
  };
}

describe("hydrate loop reunification (delegates to fetchAndStoreVideo)", () => {
  before(() => {
    const db = getDb();
    initSchema(db);
  });

  it("delegates per-video work to fetchAndStoreVideo using preFetchedDetails (no extra videos.list calls)", async () => {
    const videoIds = ["hlv1", "hlv2", "hlv3", "hlv4"];
    const detailsMap = new Map(videoIds.map((id) => [id, makeDetails(id)]));

    // Track transcript-fetcher invocations; controls mixed outcomes.
    const transcriptCalls: string[] = [];
    const mockTranscriptFetcher = async (id: string): Promise<Transcript> => {
      transcriptCalls.push(id);
      if (id === "hlv1" || id === "hlv2") return makeTranscript(id);
      if (id === "hlv3") throw new Error("No captions available");
      throw new Error("Some other transport failure");
    };

    // Simulate the serial hydrate loop (HYDRATE_TRANSCRIPT_CONCURRENCY=1).
    const outcomes = [];
    for (const id of videoIds) {
      const outcome = await fetchAndStoreVideo(id, true, {
        preFetchedDetails: detailsMap.get(id)!,
        source: "get_playlist_items",
        transcriptFetcher: mockTranscriptFetcher,
      });
      outcomes.push(outcome);
    }

    // Assertion 1: fetchAndStoreVideo was effectively invoked once per video.
    assert.strictEqual(outcomes.length, videoIds.length);

    // Assertion 2: transcriptFetcher was called exactly once per video (serial).
    assert.strictEqual(transcriptCalls.length, videoIds.length);
    assert.deepStrictEqual(transcriptCalls, videoIds);

    // Assertion 3: outcomes reflect classification correctly.
    // (Implicit proof that the metadata path was bypassed — unknown IDs
    //  cannot resolve through the real videos.list endpoint.)
    assert.strictEqual(outcomes[0].transcript, "ok");
    assert.strictEqual(outcomes[1].transcript, "ok");
    assert.strictEqual(outcomes[2].transcript, "missing"); // classifier routed
    assert.strictEqual(outcomes[3].transcript, "failed");

    // Assertion 4: DB rows carry channel_id populated from preFetchedDetails.
    const db = getDb();
    for (const id of videoIds) {
      const row = db
        .prepare(
          "SELECT channel_id, transcript_status FROM videos WHERE video_id = ?"
        )
        .get(id) as
        | { channel_id: string | null; transcript_status: string | null }
        | undefined;
      assert.ok(row, `row for ${id} must exist`);
      assert.strictEqual(
        row!.channel_id,
        `UC_${id}`,
        `channel_id must be populated for ${id}`
      );
    }
  });

  it("preserves HYDRATE_TRANSCRIPT_CONCURRENCY=1 (serial ordering)", async () => {
    const { HYDRATE_TRANSCRIPT_CONCURRENCY } = await import(
      "../tools/get-playlist-items.js"
    );
    assert.strictEqual(HYDRATE_TRANSCRIPT_CONCURRENCY, 1);
  });
});
